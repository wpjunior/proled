-- Create the database and the user for SAPL in the MySQL
--

CREATE SCHEMA IF NOT EXISTS `interlegis` DEFAULT CHARACTER SET latin1 COLLATE latin1_swedish_ci ;

GRANT ALL PRIVILEGES ON interlegis.* TO 'sapl'@'localhost' IDENTIFIED BY 'sapl';

FLUSH PRIVILEGES;

